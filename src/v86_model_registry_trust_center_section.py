def _load_json(path: Path):
    if not path.exists():
        return None
    return json.loads(path.read_text(encoding="utf-8-sig"))


def _load_csv(path: Path):
    if not path.exists():
        return []
    with path.open("r", encoding="utf-8-sig", newline="") as f:
        return list(csv.DictReader(f))


def _show_table(rows):
    if not rows:
        st.info("???? ??????? ?????? ? ?????????.")
        return

    visible_rows = []
    for row in rows:
        item = {}
        for source_key, label in COLUMN_LABELS:
            value = row.get(source_key, "")
            if source_key == "artifacts_present":
                value = f"{row.get('artifacts_present', '')}/{row.get('artifacts_expected', '')}"
            if source_key == "artifacts_expected":
                continue
            item[label] = value
        visible_rows.append(item)

    if pd is not None:
        st.dataframe(pd.DataFrame(visible_rows), use_container_width=True, hide_index=True)
    else:
        st.table(visible_rows)


