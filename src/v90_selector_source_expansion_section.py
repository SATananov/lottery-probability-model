def _load_model() -> dict[str, Any]:
    path = Path(MODEL_PATH)
    if path.exists():
        try:
            return json.loads(path.read_text(encoding="utf-8-sig"))
        except Exception:
            pass
    return build_and_save()


def render_v90_selector_source_expansion_section() -> None:
    st.title(TITLE)
    st.caption(INTRO)

    model = _load_model()
    candidates = model.get("candidates", [])

    col1, col2 = st.columns(2)
    col1.metric(_t("\u041a\u0430\u043d\u0434\u0438\u0434\u0430\u0442 \u043f\u0430\u043a\u0435\u0442\u0438"), model.get("candidate_count", 0))
    col2.metric(_t("\u041f\u0440\u043e\u0432\u0435\u0440\u0435\u043d\u0438 \u0438\u0437\u0442\u043e\u0447\u043d\u0438\u0446\u0438"), model.get("source_count", 0))

    rows = []
    for item in candidates:
        rows.append(
            {
                _t("\u041f\u0430\u043a\u0435\u0442"): item.get("package_label", ""),
                _t("\u041a\u043e\u043c\u0431\u0438\u043d\u0430\u0446\u0438\u0438"): item.get("total_combinations", 0),
                _t("\u041f\u043e\u043a\u0440\u0438\u0442\u0438 \u0447\u0438\u0441\u043b\u0430"): item.get("unique_covered_numbers", 0),
                _t("\u0420\u0438\u0441\u043a \u043e\u0442 \u043f\u0440\u0430\u0437\u0435\u043d \u043f\u0430\u043a\u0435\u0442"): f"{float(item.get('empty_risk_percent', 0.0)):.2f}%",
            }
        )

    if rows:
        st.dataframe(pd.DataFrame(rows), use_container_width=True, hide_index=True)
    else:
        st.warning(_t("\u041d\u044f\u043c\u0430 \u043d\u0430\u043c\u0435\u0440\u0435\u043d\u0438 \u043a\u0430\u043d\u0434\u0438\u0434\u0430\u0442 \u043f\u0430\u043a\u0435\u0442\u0438."))

    with st.expander(_t("\u041a\u0430\u043a\u0432\u043e \u0441\u0435 \u043f\u0440\u043e\u043c\u0435\u043d\u044f?")):
        st.markdown(
            _t(
                "- \u0421\u0435\u043b\u0435\u043a\u0442\u043e\u0440\u044a\u0442 \u0432\u0435\u0447\u0435 \u0432\u0438\u0436\u0434\u0430 \u043f\u043e\u0432\u0435\u0447\u0435 \u043f\u0430\u043a\u0435\u0442\u0438 \u043e\u0442 \u043f\u0440\u0435\u0434\u0438\u0448\u043d\u0438 \u0441\u0442\u044a\u043f\u043a\u0438.\n"
                "- \u0424\u0438\u043d\u0430\u043b\u043d\u0438\u044f\u0442 \u0438\u0437\u0431\u043e\u0440 \u0441\u0440\u0430\u0432\u043d\u044f\u0432\u0430 \u0442\u0435\u0437\u0438 \u043f\u0430\u043a\u0435\u0442\u0438 \u043f\u043e \u043c\u043e\u0434\u0435\u043b\u043d\u0430 \u0441\u0438\u043b\u0430, \u043a\u0430\u0447\u0435\u0441\u0442\u0432\u043e, \u043f\u043e\u043a\u0440\u0438\u0442\u0438\u0435 \u0438 \u0438\u0441\u0442\u043e\u0440\u0438\u0447\u0435\u0441\u043a\u0430 \u0431\u0435\u0437\u043e\u043f\u0430\u0441\u043d\u043e\u0441\u0442."
            )
        )

    st.warning(SAFE_NOTE)
